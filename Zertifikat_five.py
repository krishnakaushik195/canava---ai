THEORIE_MAX    = 66   # chars per line for Theorie/Praxis content
SUBTITLE_MAX   = 42   # CourseTitle box
CONGRATS_MAX   = 45   # Congratulations line
SECTION_MAX    = 43   # Ausbildungsziel label
