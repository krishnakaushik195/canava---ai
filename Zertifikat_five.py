def wrap_chars(text, max_chars, prefix="", continuation=""):
    """Simple char-count wrapping — used for Montserrat font boxes."""
    words = text.strip().split()
    lines, cur_words, cur_len = [], [], len(prefix)
    budget = max_chars - len(prefix)
    for word in words:
        sp = 1 if cur_words else 0
        if cur_len + sp + len(word) > budget and cur_words:
            pfx = prefix if not lines else continuation
            lines.append(pfx + ' '.join(cur_words))
            cur_words, cur_len = [word], len(continuation) + len(word)
            budget = max_chars - len(continuation)
        else:
            cur_words.append(word)
            cur_len += sp + len(word)
    if cur_words:
        pfx = prefix if not lines else continuation
        lines.append(pfx + ' '.join(cur_words))
    return '\n'.join(lines)


def build_bullet_list(section_title, items):
    """
    Build bullet list:
      Theorie:
      - Item 1
      - Item 2
    """
    lines = [section_title]
    for item in items:
        wrapped = wrap_chars(item, THEORIE_MAX, prefix="- ", continuation="  ")
        lines.append(wrapped)
    return '\n'.join(lines)


